//Random float function from stack overflow
float RandomFloat(float min, float max)
{
    //this  function assumes max > min, you may want 
    //more robust error checking for a non-debug buil
    assert(max > min); 
    float random = ((float) rand()) / (float) RAND_MAX;

    //generate a float between the min and the max
    float range = max - min;  
    return (random*range) + min;
}